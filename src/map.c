int is_wall(float x, float y) {
  int map_x = (int)x;
  int map_y = (int)y;

  if (map_x >= 0 && map_x < MAP_WIDTH && map_y < MAP_HEIGHT) {
    return worldMap[map_x][map_y];
  }

  return 1; // If the indices are out of bounds, meaning the position is outside the game world, the function returns 1.
}
